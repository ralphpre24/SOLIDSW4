package ProblemOne;

public class PetBehavior {
    private PetInfo petInfo;

    PetBehavior(PetInfo petInfo){
        this.petInfo = petInfo;
    }

    public String makeSound(){
        return petInfo.getPetName() + "make a sound!";
    }
    public String eat(){
        return petInfo.getPetName() + "is eating";
    }
    
}
